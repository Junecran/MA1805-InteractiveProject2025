// ----------------- GAME LOOP -----------------
function draw() {
  background(233, 236, 240);

  if (!showMainMenu) player.update();

  if (showTemporaryMessage) {
    drawMaze();
    player.show();
    enemies.forEach(e => e.show());

    push();
    fill(255);
    textAlign(CENTER, CENTER);
    textSize(50);
    text(overlayMessage, width / 2, height / 2);
    pop();

    messageTimer--;
    if (messageTimer <= 0) {
      showTemporaryMessage = false;
      showMainMenu = true;
    }
    return;
  }
  

  drawMaze();
  player.show();
  enemies.forEach(e => {
    if (!showMainMenu) e.chase(player);
    e.show();
  });

  // Win condition
  if (!gameEndEvent&& maze[player.y][player.x] === 3) {
    gameEndEvent= true;
    overlayMessage = "You Win!";
    showTemporaryMessage = true;
    messageTimer = messageDelay;
  }

  // Lose condition
  enemies.forEach(e => {
    if (!gameEndEvent&& e.x === player.x && e.y === player.y) {
      gameEndEvent= true;
      overlayMessage = "Game Over!";
      showTemporaryMessage = true;
      messageTimer = messageDelay;
    }
  });

  // Menu Drawing //
  if (showMainMenu) {
  if (showInfoMenu) {
   background(255);
   image(infoMenu.img, infoMenu.x, infoMenu.y, infoMenu.width, infoMenu.height);
   drawBackButton(infoMenu.img, infoMenu.x, infoMenu.y, infoMenu.width, infoMenu.height);
  } else {
   background(255);
   image(mainMenu.img, mainMenu.x, mainMenu.y, mainMenu.width, mainMenu.height);
   drawResetButton();
   drawSecondButton();
  }
  return;
}
}

// ----------------- MAZE -----------------
function drawMaze() {
  noStroke(); // remove outlines

  for (let y = 0; y < rows; y++) {
    for (let x = 0; x < cols; x++) {

      if (maze[y][x] === 1) fill(89, 90, 92); // maze colour
      else if (maze[y][x] === 3) fill(0, 255, 0);
      else fill(233, 236, 240);

      rect(
        offsetX + x * tileSize - 0.5,
        offsetY + y * tileSize - 0.5,
        tileSize + 1,
        tileSize + 1
      );
    }
  }
}


// ----------------- RESET -----------------
function resetGame() {
  player = new Player(39, 41);
  enemies = [
    new Enemy(9, 9),
    new Enemy(75, 75),
    new Enemy(9, 75),
    new Enemy(75, 9),
    new Enemy(60, 30),
    new Enemy(30, 60),
    new Enemy(12, 5, true)
  ];
  gameEndEvent= false;
  showMainMenu = true;
  showTemporaryMessage = false;
  overlayMessage = "";
}

// ----------------- GAME LOGIC -----------------
function gameStartEvent() {
  let startEnemies = [[9,9],[75,75],[9,75],[75,9],[60, 30],[30, 60],[12,5]];
  if (player.x !== 39 || player.y !== 41) return false;
  for (let i=0;i<enemies.length;i++) {
    if (enemies[i].x !== startEnemies[i][0] || enemies[i].y !== startEnemies[i][1]) return false;
  }
  return true;
}

